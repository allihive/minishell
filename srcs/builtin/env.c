#include <minishell.h>

void	env(t_shell *ms)
{
	int		i;
	// int		j;
	char	**tmp;

	i = 0;
	tmp = ms->envp;
	while (i < ms->envp_size)
	{
		ft_putstr_fd(tmp[i], 1);
		ft_putchar_fd('\n', 1);
		i++;
	}
}
